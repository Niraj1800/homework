//Write a C program to check whether a number is negative, positive or zero.

#include<stdio.h>
int main() {
    int num;
    printf("Enter a number: ");
    scanf("%d", &num);
    
    if (num > 0) {
        printf("The number is Positive");
    }
    else if (num < 0) {
        printf("The number is Negative");
    }
    else {
        printf("The number is Zero");
    }
    
    return 0;
}

//output
//Enter a number: -2
//The number is Negative
